    public void setEmpleado(boolean edit, Empleado empleado, GestionarEmpleados gestionarEmpleados) {
        this.gestionarEmpleados = gestionarEmpleados;
        this.isEdit = edit;
        if (isEdit) {
            this.labelTitle.setText("Editar empleado");
            this.usernameText.disable();
            this.usernameText.setText(empleado.getUsuario());
            this.nombreText.setText(empleado.getFirstname());
            this.apellidoText.setText(empleado.getLastname());
            this.correoText.setText(empleado.getEmail());
            this.btnCrear.setText("Editar");
        }
    }

