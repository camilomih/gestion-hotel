    private void tableReservasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableReservasMouseClicked
        JTable source = (JTable) evt.getSource();
        int row = source.rowAtPoint(evt.getPoint());
        //int column = source.columnAtPoint(evt.getPoint());
        Integer id = (Integer) source.getModel().getValueAt(row, 0);
        String cedula = (String) source.getModel().getValueAt(row, 1);
        String nombre = (String) source.getModel().getValueAt(row, 2);
        String habitacion = (String) source.getModel().getValueAt(row, 3);
        Date fechaInicio = (Date) source.getModel().getValueAt(row, 4);
        Date fechaFin = (Date) source.getModel().getValueAt(row, 5);
        boolean estado = "Activa".equals((String) source.getModel().getValueAt(row, 6));

        Reserva reserva = new Reserva(id, cedula, nombre, habitacion, fechaInicio, fechaFin, estado);
        reservaEdit = reserva;
    }//GEN-LAST:event_tableReservasMouseClicked

