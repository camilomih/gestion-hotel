    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        if (reservaEdit == null) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un registro");
            return;
        }

        if (!reservaEdit.isActivo()) {
            JOptionPane.showMessageDialog(null, "Esta reserva ya no se puede editar, porque esta vencida.");
            return;
        }

        CrearReserva cr = new CrearReserva();
        cr.setLocationRelativeTo(null);
        cr.setVisible(true);
        cr.setReservaData(true, reservaEdit, this);
    }//GEN-LAST:event_jMenuItem2ActionPerformed

    private void jMenuItem3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem3ActionPerformed
        if (reservaEdit == null) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un registro");
            return;
        }

        reservaService.deleteReserva(reservaEdit);
        loadData();
        JOptionPane.showMessageDialog(null, "Se elimino correctamente la reserva");
    }//GEN-LAST:event_jMenuItem3ActionPerformed

