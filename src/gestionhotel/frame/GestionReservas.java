    public void loadData() {
        try {
            reservas = this.reservaService.getReservas();
            this.removeAllRows();
            DefaultTableModel model = (DefaultTableModel) this.tableReservas.getModel();
            reservas.forEach(h -> {
                model.addRow(new Object[]{h.getId(), h.getCedulaCliente(), h.getNombreCliente(), h.getIdHabitacion(), h.getFechaInicio(), h.getFechaFin(), h.isActivo() ? "Activa" : "Vencida"});
            });
        } catch (Exception ex) {
            Logger.getLogger(GestionarEmpleados.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void removeAllRows() {
        DefaultTableModel model = (DefaultTableModel) this.tableReservas.getModel();
        int rowCount = model.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            model.removeRow(i);
        }
    }

