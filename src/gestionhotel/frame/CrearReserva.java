    public void setReservaData(boolean isEdit, Reserva reserva, GestionReservas gestionReservas) {
        this.gestionReservas = gestionReservas;
        this.isEdit = isEdit;
        if (isEdit) {
            this.idReserva = reserva.getId();
            this.textCed.setText(reserva.getCedulaCliente());
            this.textNom.setText(reserva.getNombreCliente());
            this.comboHab1.addItem(reserva.getIdHabitacion());
            this.comboHab1.setSelectedItem(reserva.getIdHabitacion());
            this.comboHab1.disable();
            this.fechaInicioDater.setDate(reserva.getFechaInicio());
            this.fechaFinDater.setDate(reserva.getFechaFin());
            this.chekcActivo.setSelected(reserva.isActivo());
            this.jLabel1.setText("Editar reserva");
            this.btnCrear.setText("Editar");

        }

    }

    public void loadHabitacionesLibres() {
        List<Habitacion> habitacionesLibres = habitacionService.getHabitacionesLibres();
        habitacionesLibres.forEach(hab -> {
            this.comboHab1.addItem((hab.getCodigo()));
        });
    }

