    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        String cedula = this.textCed.getText();
        String nombre = this.textNom.getText();
        String habitacion = (String) this.comboHab1.getSelectedItem();
        Date fechaInicio = this.fechaInicioDater.getDate();
        Date fechaFin = this.fechaFinDater.getDate();
        boolean activo = this.chekcActivo.isSelected();

        if (cedula.isEmpty()) {
            JOptionPane.showMessageDialog(null, "La cedula del cliente es obligatoria");
            return;
        }

        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(null, "El nombre del cliente es obligatorio");
            return;
        }

        if (habitacion.equals("Seleccionar opci√≥n")) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una habitacion");
            return;
        }

        if (fechaInicio == null) {
            JOptionPane.showMessageDialog(null, "Fecha inicio es obligatorio");
            return;
        }

        if (fechaFin == null) {
            JOptionPane.showMessageDialog(null, "Fecha fin es obligatorio");
            return;
        }

        Reserva reserva = new Reserva();
        reserva.setCedulaCliente(cedula);
        reserva.setNombreCliente(nombre);
        reserva.setIdHabitacion(habitacion);
        reserva.setFechaInicio(fechaInicio);
        reserva.setFechaFin(fechaFin);
        reserva.setActivo(activo);
        if (isEdit) {
            reserva.setId(idReserva);
            reservaService.editReserva(reserva);
            this.gestionReservas.loadData();
            JOptionPane.showMessageDialog(null, "Reserva editada correctamente");
        } else {
            reservaService.crearReserva(reserva);
            this.gestionReservas.loadData();
            JOptionPane.showMessageDialog(null, "Reserva creada correctamente");
        }
        dispose();
    }//GEN-LAST:event_btnCrearActionPerformed

